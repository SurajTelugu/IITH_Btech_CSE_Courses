#include <iostream>
using namespace std;
int main()
{
  float lnth,brth,area ;

  cout<<"Enter the length of rectangle:";
  cin>>lnth;

  cout<<"Enter the breadth of rectangle:";
  cin>>brth;

  area = lnth * brth ;

  cout<<"Area of rectangle with length "<<lnth<<" and breadth "<<brth<<" is "<<area << endl ;

return 0;
}